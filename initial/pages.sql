<h1>Was ist Yoga?</h1>
<p>&nbsp;</p>
<div>
<div>Yoga ist ein ganzheitlicher, hinduistisch gepr&auml;gter &Uuml;bungsweg f&uuml;r die Seele, den Geist und den K&ouml;rper. Jedes Asana (&Uuml;bungspraxis) ist neben der k&ouml;rperlichen Kr&auml;ftigung, ein innerer kreativer Prozess, der uns neue Dimensionen er&ouml;ffnen kann.Heute kennen wir im Westen vor allem den Hatha-Yoga, der uns den Weg der inneren Erleuchtung weist. Mit ethischen Handlungsans&auml;tzen, t&auml;glicher &Uuml;bungspraxis- Asana, Atem&uuml;bungen- Pranayama - &Uuml;bungen zur Beruhigung und Reinigung auf mentaler und energetischen Ebene, f&uuml;hrt uns der Hatha- Yoga in die Stille zur Konzentrationsf&auml;higkeit. Hier beginnt der &Uuml;bergang zur Meditation, zu unserem Selbst. Diesen Schulungsweg finden wir in den verschiedenen Yogaschulen: z.B. Patanjali Yoga Sutra beinhaltet der achtfache Pfad, auch als Spiegel der Seele genannt; In der Bhagavad Gita, wird der Bakthi Marga als der Yoga Weg der Hingabe dargestellt. Die verschiedenen Yogaschulen haben sich in der Entstehungsgeschichte gegenseitig gepr&auml;gt und beeinflusst.</div>
</div>
<h1>&nbsp;</h1>
<h1>Gesundheit</h1>
<p>&nbsp;</p>
<div>
<div>Heutzutage ist der Yoga immer noch oder gerade wieder aktuell. In der modernen Zeit, in welcher wir st&auml;ndig gefordert oder sogar gestresst uns f&uuml;hlen, kann Yoga uns helfen, innere Ruhe zu erlangen. T&auml;glich kurze Zeit sich selber zu g&ouml;nnen, f&ouml;rdert unsere Gesundheit und Ausgeglichenheit, daraus kann Wohlbefinden und Zufriedenheit entstehen.Die K&ouml;rper&uuml;bungen, Asana helfen unseren oft einseitig belasteten K&ouml;rper auszugleichen: mit Aufrichtung, Lockerung, Dehnungen und Kr&auml;ftigung der Muskulatur und Balance&uuml;bungen. Durch die K&ouml;rperarbeit erreichen wir mehr Stabilit&auml;t und zugleich Leichtigkeit: Sthiram Sukham Asana, PYS. 2,6.Die Atem&uuml;bungen, Pranayama f&uuml;hren uns mit der Konzentration auf den Atem, in die Stille und schenken uns Ruhe und Gelassenheit. Der Energiehaushalt wird gereinigt und harmonisiert. Sind die Gedanken durch die Konzentration auf den Atem unter Kontrolle, erweitert sich das Bewusstsein. In der Meditation kann der &Uuml;bende die Verbindung zu seinem Selbst neu erfahren und sich mit der Einheit verbinden. Diese Erfahrungen erm&ouml;glichen uns, die Lebenshaltung positiv zu gestalten und innere Zufriedenheit zu erreichen.</div>
